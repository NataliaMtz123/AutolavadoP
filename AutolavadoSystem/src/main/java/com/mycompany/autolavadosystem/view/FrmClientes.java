    private void listarClientes(){
       modeloTabla.setRowCount(0);
       List<ClientesModel> clientes = ClientesControlador.obtenerTodosClientes();
       for(ClientesModel c: clientes){
           modeloTabla.addRow(new Object[]{c.getIdCliente(), c.getNombre(), c.getPrimerApellido(), c.getSegundoApellido(),
           c.getDireccion(), c.getTelefono(), c.getCorreo(), c.getPassword()});
       }
    }
    
    private void insertarCliente(){
        String nombre = txtNombre.getText().trim();
        String primerApellido = txtPrimerApellido.getText().trim();
        String segundoApellido = txtSegundoApellido.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String correo = txtCorreo.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        
        if(!nombre.isEmpty() || !primerApellido.isEmpty() || !direccion.isEmpty() || !telefono.isEmpty() || !correo.isEmpty()
                || !password.isEmpty()){
            ClientesControlador.insertarCliente(new ClientesModel(nombre,primerApellido,segundoApellido,direccion,telefono,correo,password));
            listarClientes(); 
        }else{
            JOptionPane.showMessageDialog(this, "Llenar todos los campos");
        }
    }
    
    private void buscarCliente(){
        String nombre = JOptionPane.showInputDialog("Ingrese el nombre");
        if(!nombre.isEmpty()){
            modeloTabla.setRowCount(0);
            List<ClientesModel> clientes = ClientesControlador.buscarClientePorNombre(nombre);
            for(ClientesModel c: clientes){
                modeloTabla.addRow(new Object[]{c.getIdCliente(), c.getNombre(), c.getPrimerApellido(), c.getSegundoApellido(),
                c.getDireccion(), c.getTelefono(), c.getCorreo(), c.getPassword()});
            }       
        }else{
            JOptionPane.showMessageDialog(this, "Ingresa un nombre");
        }
    }
    
    private void seleccionarCliente(){
        tblClientes.getSelectionModel().addListSelectionListener(e -> {
        if(!e.getValueIsAdjusting()) {
            int fila = tblClientes.getSelectedRow();
            if (fila != -1){
                idCliente = Integer.parseInt(tblClientes.getValueAt(fila,0).toString());
                txtNombre.setText(tblClientes.getValueAt(fila, 1).toString());
                txtPrimerApellido.setText(tblClientes.getValueAt(fila, 2).toString());
                txtSegundoApellido.setText(tblClientes.getValueAt(fila, 3).toString());
                txtDireccion.setText(tblClientes.getValueAt(fila, 4).toString());
                txtTelefono.setText(tblClientes.getValueAt(fila, 5).toString());
                txtCorreo.setText(tblClientes.getValueAt(fila, 6).toString());
                txtPassword.setText(tblClientes.getValueAt(fila, 7).toString());
            }else{
                JOptionPane.showMessageDialog(this, "No existen Clientes");
            }

        }
    });
    }
    private void eliminarCliente(){
        int fila = tblClientes.getSelectedRow();
        if(fila != -1){
            int id = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
            ClientesControlador.eliminarCliente(id);
            listarClientes();
        }else{
            JOptionPane.showMessageDialog(this, "Selecciona el usuario a eliminar");
        }
    }
    
    private void modificarCliente(){
        String nombre = txtNombre.getText().trim();
        String primerApellido = txtPrimerApellido.getText().trim();
        String segundoApellido = txtSegundoApellido.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String correo = txtCorreo.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        
        if(!nombre.isEmpty() || !primerApellido.isEmpty() || !direccion.isEmpty() || !telefono.isEmpty() || !correo.isEmpty()
                || !password.isEmpty()){
            ClientesControlador.actualizarCliente(new ClientesModel(this.idCliente,nombre,primerApellido,segundoApellido,direccion,telefono,correo,password));
            listarClientes(); 
        }else{
            JOptionPane.showMessageDialog(this, "Llenar todos los campos");
        }
    }

