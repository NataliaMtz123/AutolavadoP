    private void listarUsuarios(){
        modeloTabla.setRowCount(0);
        List<UsuarioModel> usuarios = UsuarioControlador.obtenerTodos();
        for(UsuarioModel r : usuarios){
            modeloTabla.addRow(new Object[]{r.getIdUsuario(), r.getNombre(), r.getPrimerApellido(), r.getSegundoApellido(),
            r.getDireccion(), r.getTelefono(), r.getCorreo(), r.getUsuario(), r.getPassword(), r.getFechaNacimiento(), r.getIdRol()});
        }
    }
    private void insertarUsuario(){
        String nombre = txtNombre.getText().trim();
        String primerApellido = txtPrimerApellido.getText().trim();
        String segundoApellido = txtSegundoApellido.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String correo = txtCorreo.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        String fechaNacimiento = txtAnio.getText()+"-"+txtMes.getText()+"-"+txtDia.getText();
        int idRol = rolObtenido;
        
        if (!nombre.isEmpty() || !primerApellido.isEmpty() || !direccion.isEmpty() || !telefono.isEmpty() || !correo.isEmpty() || !usuario.isEmpty() ||
                !password.isEmpty() || !fechaNacimiento.isEmpty()){
            UsuarioControlador.insertarUsuario(new UsuarioModel(nombre,primerApellido,segundoApellido,direccion,telefono,correo,usuario,password,fechaNacimiento,idRol));
            listarUsuarios();
    }else{
         JOptionPane.showMessageDialog(this, "Llenar todos los campos");
        }
    }
    private void  buscarUsuario(){
        String nombre  = JOptionPane.showInputDialog("Ingrese el nombre");
        if (!nombre.isEmpty()){
            modeloTabla.setRowCount(0);
            List<UsuarioModel> usuarios = UsuarioControlador.buscarUsuarioPorNombre(nombre);
            for(UsuarioModel r: usuarios){
                modeloTabla.addRow(new Object[]{r.getIdUsuario(), r.getNombre(), r.getPrimerApellido(), r.getSegundoApellido(),
            r.getDireccion(), r.getTelefono(), r.getCorreo(), r.getUsuario(), r.getPassword(), r.getFechaNacimiento(), r.getIdRol()});
            }
        }else{
            JOptionPane.showMessageDialog(this,"Ingresa un nombre");
        }
        
    }
    
    private void seleccionarUsuario(){
        tblUsuarios.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            int fila = tblUsuarios.getSelectedRow();
            if (fila != -1){
                idUsuario = Integer.parseInt(tblUsuarios.getValueAt(fila,0).toString());
                txtNombre.setText(tblUsuarios.getValueAt(fila, 1).toString());
                txtPrimerApellido.setText(tblUsuarios.getValueAt(fila, 2).toString());
                txtSegundoApellido.setText(tblUsuarios.getValueAt(fila, 3).toString());
                txtDireccion.setText(tblUsuarios.getValueAt(fila, 4).toString());
                txtTelefono.setText(tblUsuarios.getValueAt(fila, 5).toString());
                txtCorreo.setText(tblUsuarios.getValueAt(fila, 6).toString());
                txtUsuario.setText(tblUsuarios.getValueAt(fila, 7).toString());
                txtPassword.setText(tblUsuarios.getValueAt(fila, 8).toString());
                String fechaCompleta = tblUsuarios.getValueAt(fila, 9).toString();
                String[] partesFecha = fechaCompleta.split("-");
                String anio = partesFecha[0];
                String mes = partesFecha[1];
                String dia = partesFecha[2];
                txtAnio.setText(anio);
                txtMes.setText(mes);
                txtDia.setText(dia);
                
            }else{
            JOptionPane.showMessageDialog(this,"No existen usuarios");
            }
        }
      });
        
    }
    private void eliminarUsuario(){
        int fila = tblUsuarios.getSelectedRow();
        if (fila != -1){
            int id = Integer.parseInt(tblUsuarios.getValueAt(fila, 0).toString());
            UsuarioControlador.eliminarUsuario(id);
            listarUsuarios();
        }else{
            JOptionPane.showMessageDialog(this, "Selecciona el usuario a eliminar");
        }
    }
    private void modificarUsuario(){
        String nombre = txtNombre.getText().trim();
        String primerApellido = txtPrimerApellido.getText().trim();
        String segundoApellido = txtSegundoApellido.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String correo = txtCorreo.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        String fechaNacimiento = txtAnio.getText()+"-"+txtMes.getText()+"-"+txtDia.getText();
        int idRol = rolObtenido;
        
        if (!nombre.isEmpty() || !primerApellido.isEmpty() || !direccion.isEmpty() || !telefono.isEmpty() || !correo.isEmpty() || !usuario.isEmpty() ||
                !password.isEmpty() || !fechaNacimiento.isEmpty()){
            UsuarioControlador.actualizarUsuario(new UsuarioModel(this.idUsuario,nombre,primerApellido,segundoApellido,direccion,telefono,correo,usuario,password,fechaNacimiento,idRol));
            listarUsuarios();
    }else{
         JOptionPane.showMessageDialog(this, "Llenar todos los campos");
        }
        
    }
     private void obtenerRol(){
        cmbRoles.removeAllItems();
        List<RolModel> roles = RolesControlador.obtenerTodos();
        for (RolModel rol: roles){
            cmbRoles.addItem(rol);
        }
    }
    private void obtenerIdRol(){
        cmbRoles.addActionListener(e -> {
        RolModel seleccionado = (RolModel) cmbRoles.getSelectedItem();
        if(seleccionado != null){
            rolObtenido = seleccionado.getIdRol();
            
        }
        });
    }
              
